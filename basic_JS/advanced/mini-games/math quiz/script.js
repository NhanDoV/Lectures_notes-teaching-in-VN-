let currentSet = -1;
let currentQuestion = 0;
let score = 0;
let answered = false;

const screens = {
    menu: document.getElementById('menu-screen'),
    game: document.getElementById('game-screen'),
    end: document.getElementById('end-screen')
};

document.querySelectorAll('.set-btn').forEach(btn => {
    btn.addEventListener('click', () => {
        currentSet = parseInt(btn.dataset.set);
        startQuiz();
    });
});

function startQuiz() {
    currentQuestion = 0;
    score = 0;
    screens.menu.classList.remove('active');
    screens.game.classList.add('active');
    showQuestion();
}

function showQuestion() {
    const quiz = quizData[currentSet];
    const q = quiz.questions[currentQuestion];
    
    document.getElementById('set-name').textContent = quiz.name;
    const total = quiz.questions.length;
    document.getElementById('question-progress').textContent =
        `CÃ¢u ${currentQuestion + 1}/${total}`;

    document.getElementById('score').textContent = score;
    
    const imgDiv = document.getElementById('question-image');
    if (q.image) {
        imgDiv.innerHTML = `<img src="${q.image}" alt="HÃ¬nh cÃ¢u há»i">`;
    } else {
        imgDiv.innerHTML = 'â“';
    }
    
    document.getElementById('question-text').textContent = q.question;
    
    const optionsDiv = document.getElementById('options');
    optionsDiv.innerHTML = '';
    q.options.forEach((option, index) => {
        const btn = document.createElement('div');
        btn.className = 'option';
        btn.textContent = option;
        btn.dataset.index = index;
        btn.addEventListener('click', () => selectOption(index, q.correct));
        optionsDiv.appendChild(btn);
    });
    
    document.getElementById('next-btn').style.display = 'none';
    answered = false;
}

function selectOption(selected, correct) {
    if (answered) return;
    
    answered = true;
    const options = document.querySelectorAll('.option');
    
    options.forEach((opt, index) => {
        if (index === correct) {
            opt.classList.add('correct');
        } else if (index === selected && selected !== correct) {
            opt.classList.add('incorrect');
        }
        
        opt.style.pointerEvents = 'none';
    });
    
    if (selected === correct) {
        score++;
        document.getElementById('score').textContent = score;
    }
    
    setTimeout(() => {
        document.getElementById('next-btn').style.display = 'block';
    }, 1000);
}

document.getElementById('next-btn').addEventListener('click', nextQuestion);

function nextQuestion() {
    currentQuestion++;
    const total = quizData[currentSet].questions.length;

    if (currentQuestion < total) {
        showQuestion();
    } else {
        endQuiz();
    }
}

